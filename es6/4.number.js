/**
 *  Number.isFinite(), Number.isNaN()
 */
Number.isFinite(15); // true
Number.isFinite(0.8); // true
Number.isFinite(NaN); // false
Number.isNaN(NaN) // true
Number.isNaN(15) // false
Number.isNaN('15') // false
/**
 *  Number.isInteger()
 */
Number.isInteger(25) // true
Number.isInteger(25.1) // false
